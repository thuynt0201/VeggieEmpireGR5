package com.project.models;

public class ThongBao {
    public String Tenthongbao;
    public String Mathongbao;
    public String Motathongbao;
    public String Thoigianthongbao;
    public String Ngaythongbao;
    public int Hinhthongbao;

    public ThongBao(String tenthongbao, String mathongbao, String motathongbao, String thoigianthongbao, String ngaythongbao, int hinhthongbao) {
        Tenthongbao = tenthongbao;
        Mathongbao = mathongbao;
        Motathongbao = motathongbao;
        Thoigianthongbao = thoigianthongbao;
        Ngaythongbao = ngaythongbao;
        Hinhthongbao = hinhthongbao;
    }
}
